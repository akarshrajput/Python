# escape characters
print("Hello \nPython") # to print in next line
print("Hello \'Python\"") # to print in single quotes
print("Hello \"Python\"") # to print in double quotes
print("Hello \tPython") # to print tab space
print("H\ne\nl\nl\no")
print("It\'s getting colder")
print("Its \\ getting \\ colder")
print(repr("It\'s \ngetting \"colder\"")) # repr() use to print all the escape character without running  